package ch06.book.Exercise.p16;

public class Printer {

	void println(int i) {
//		i = 10;
		System.out.println(i);
	}
	
	void println(boolean a) {
//		a = true;
		System.out.println(a);
	}
	
	void println(double a) {
//		a = 5.7;
		System.out.println(a);
	}
	
	void println(String a) {
//		a = "홍길동";
		System.out.println(a);
	}
	
}
